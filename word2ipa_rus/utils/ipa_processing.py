            "æ",
            "ɐ",
            "ɕ",
            "ɕː",
            "ə",
            "ɛ",
            "ɡ",
            "ɡʲ",
            "ɡː",
            "ɨ",
            "ɪ",
            "ɵ",
            "ʂ",
            "ʂː",
            "ʈ͡ʂ",
            "ʉ",
            "ʊ",
            "ʐ",
            "ʐː",
