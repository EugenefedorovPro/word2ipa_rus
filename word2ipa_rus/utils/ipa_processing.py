            " ",
            "a",
            "b",
            "bʲ",
            "bʲː",
            "bː",
            "d",
            "dʲ",
            "dʲː",
            "dː",
            "d͡z",
            "d͡zʲ",
            "e",
            "f",
            "fʲ",
            "fʲː",
            "i",
            "j",
            "jː",
            "k",
            "kʲ",
            "kʲː",
            "kː",
            "lʲ",
            "lʲː",
            "lˠ",
            "lˠː",
            "m",
            "mʲ",
            "mʲː",
            "mː",
            "n",
            "nʲ",
            "nʲː",
            "nː",
            "o",
            "p",
            "pʲ",
            "pʲː",
            "pː",
            "r",
            "rʲ",
            "rʲː",
            "rː",
            "s",
            "sʲ",
            "sʲː",
            "sː",
            "t",
            "tʲ",
            "tʲː",
            "tː",
            "t͡s",
            "t͡sʲ",
            "t͡sː",
            "t͡ɕ",
            "t͡ɕː",
            "u",
            "v",
            "vʲ",
            "vʲː",
            "vː",
            "x",
